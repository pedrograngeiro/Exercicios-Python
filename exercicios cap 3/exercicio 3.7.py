# Faça um programa que peça dois números inteiros. Imprima a soma desses dois números na tela.
# Input dos dois números
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# Mostrar na tela o primeiro e segundo número - "\n" para pular linha
print("O primeiro número é: %d \n"
      "E o segundo número é: %d" % (num1, num2))